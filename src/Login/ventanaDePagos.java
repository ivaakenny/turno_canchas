    private void btnReservarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReservarActionPerformed
        
        
        if (rbtnEfectivo.isSelected()) 
        {
         JOptionPane.showMessageDialog(this, "Ud reservo con efectivo.");
         Portada Po = new Portada();
                            Po.setVisible(true);
                            this.dispose();
                            Po.pack();
        }
        else{
            if (rbtnMercadoPago.isSelected()) {
                JOptionPane.showMessageDialog(this, "Ud. reservo con Mercado pago");
                Portada Po = new Portada();
                            Po.setVisible(true);
                            this.dispose();
                            Po.pack();
            }
            else
            {
                if (rbtnPaypal.isSelected()) {
                    JOptionPane.showMessageDialog(this, "Ud. reservo con Paypal");
                    Portada Po = new Portada();
                            Po.setVisible(true);
                            this.dispose();
                            Po.pack();
                }
                else
                {
                    if (rbtnCredito.isSelected()) {
                        JOptionPane.showMessageDialog(this, "Ud. reservo con credito");
                        Portada Po = new Portada();
                            Po.setVisible(true);
                            this.dispose();
                            Po.pack();
                    
                    }
                    else{
                        if (rbtnDebito.isSelected()) {
                            JOptionPane.showMessageDialog(this, "Ud. reservo con debito");
                          
                            Portada Po = new Portada();
                            Po.setVisible(true);
                            this.dispose();
                            Po.pack();
                        }else
                        {
                            JOptionPane.showMessageDialog(this, "Ud. debe seleccionar una opcion de pago");
                        }
                    }
   
                }
            }
        }
        
      
        
        
        
    }//GEN-LAST:event_btnReservarActionPerformed

